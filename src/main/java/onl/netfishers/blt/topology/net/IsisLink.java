package onl.netfishers.blt.topology.net;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlRootElement;

/**
 * @author amoretti
 *
 */
@XmlRootElement
@XmlAccessorType(value = XmlAccessType.NONE)
public class IsisLink extends Link {
	private long localIdentifier;
	private long remoteIdentifier;
	
	public IsisLink() {

	}

	/**
	 * @return the localIdentifier
	 */
	public long getLocalIdentifier() {
		return localIdentifier;
	}

	/**
	 * @param localIdentifier the localIdentifier to set
	 */
	public void setLocalIdentifier(long localIdentifier) {
		this.localIdentifier = localIdentifier;
	}

	/**
	 * @return the remoteIdentifier
	 */
	public long getRemoteIdentifier() {
		return remoteIdentifier;
	}

	/**
	 * @param remoteIdentifier the remoteIdentifier to set
	 */
	public void setRemoteIdentifier(long remoteIdentifier) {
		this.remoteIdentifier = remoteIdentifier;
	}


}
